    EXPORT auto call(auto func, auto&&... args){
        using R = std::invoke_result_t<decltype(func), Gl&, decltype(args)...>;

        if constexpr (std::is_same_v<R, void>){
            callSimple([func, args...](Gl& gl){
                func(gl, args...);
            });
        } else {
            Value<R> result;
            callSimple([result, func, args...](Gl& gl){
                *result = func(gl, args...);
            });
            return result;
        }
    }

